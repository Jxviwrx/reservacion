    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        SessionFactory sessionFactory = new Configuration().configure().buildSessionFactory();
        Session session = sessionFactory.openSession();
        session.beginTransaction();
        
    if(idProveedores != null) {
        proveedores proveedores = session.get(proveedores.class,idProveedores);
                
        proveedores.setNombre_proveedor(txtNombre.getText());
        proveedores.setNombre_contacto(txtContacto.getText());
        proveedores.setDireccion(txtDireccion.getText());
        proveedores.setCiudad(txtDireccion.getText());
        proveedores.setCodigo_postal(txtDireccion.getText());
        proveedores.setPais(txtPais.getText());
        proveedores.setTipo_pago(txtTipoPago.getText());
        proveedores.setTelefono(txtTelefono.getText());
        proveedores.setEmail(txtEmail.getText());

        session.update(proveedores);
        idProveedores = null;
    }
        else {
        
        proveedores prove = new proveedores();
        prove.setNombre_proveedor(txtNombre.getText());
        prove.setNombre_contacto(txtContacto.getText());
        prove.setDireccion(txtDireccion.getText());
        prove.setCiudad(txtCiudad.getText());
        prove.setCodigo_postal(txtCodigo.getText());
        prove.setPais(txtPais.getText());
        prove.setTipo_pago(txtTipoPago.getText());
        prove.setTelefono(txtTelefono.getText());
        prove.setEmail(txtEmail.getText());
        
        session.save(prove);
}
      
        session.getTransaction().commit();
        session.close();
        
        JOptionPane.showMessageDialog(null,"El producto se guardo correctamente");
        
        txtNombre.setText("");
        txtContacto.setText("");
        txtDireccion.setText("");
        txtCiudad.setText("");
        txtCodigo.setText("");
        txtPais.setText("");
        txtTipoPago.setText("");
        txtTelefono.setText("");
        txtEmail.setText("");

        llenarTable();    
                                
     // TODO add your handling code here:
    }//GEN-LAST:event_btnGuardarActionPerformed

