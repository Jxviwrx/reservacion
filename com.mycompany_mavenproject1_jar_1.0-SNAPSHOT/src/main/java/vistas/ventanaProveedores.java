    public void llenarTable(){
        List<proveedores> proveedores = obtenerProveedores();
        
        int filas = tbl.getRowCount();
        for(int i = 0; i< filas; i++){
            tbl.removeRow(0);
        }
        
        for(proveedores prove : proveedores){
            tbl.addRow(new Object[]{prove.getId(),prove.getNombre_proveedor(),prove.getNombre_contacto(),prove.getDireccion(),prove.getCiudad(),prove.getCodigo_postal(),prove.getPais(),prove.getTipo_pago(),prove.getTelefono(),prove.getEmail()});
        }
    }
    
    public void eliminarProveedores(Long id){
        sessionFactory = new Configuration().configure().buildSessionFactory();
        Session session = sessionFactory.openSession();
        Transaction tx = null;
        
        try {
            tx = session.beginTransaction();
            proveedores proveedores = session.get(proveedores.class, id);

            if (proveedores !=null) {
                session.delete(proveedores);
            }
            tx.commit();
        } catch (Exception e){

        }
        
    session.close();

   }
    
    public List<proveedores> obtenerProveedores() {
        sessionFactory = new Configuration().configure().buildSessionFactory();
        Session session = sessionFactory.openSession();
        Transaction tx = null;
        List<proveedores> proveedores = null;
        
        try{
            
            tx = session.beginTransaction();
            Query<proveedores> query = session.createQuery("FROM proveedores",proveedores.class);
            proveedores = query.list();
            
            tx.commit();
        }catch(Exception e){
            
        }
        session.close();
        
        return proveedores;
    }
